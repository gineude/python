"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Gineude Silvestre')
# Truque
print('Gineude "Silvestre"')

# Aspas duplas
print("Gineude Silvestre")
# Truque
print("Gineude 'Silvestre'")

# Escape
print("Gineude \" Silvestre")

# r
print(r"Gineude \" Silvestre\"")